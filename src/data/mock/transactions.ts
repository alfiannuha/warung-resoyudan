function makeDate(daysAgo: number, hours: number, minutes: number): string {
  const d = new Date("2026-07-08T06:00:00.000Z");
  d.setDate(d.getDate() - daysAgo);
  d.setHours(hours, minutes, 0, 0);
  return d.toISOString();
}

